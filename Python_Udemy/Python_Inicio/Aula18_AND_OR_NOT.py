# Operadores Lógicos
# and (e) or (ou) not (não) 

# São considerados falsy os valores :
# 0 || 0.0 || ''(String vazia|OBS: Espaço é considerado valor) || False
# Também existe o tipo NONE que é usado para representar um não valor (valor que não existe)


# and - Todas as condições precisam ser verdadeiras
# Se qualquer valor for falso a expressão inteira será avaliada naquele valor falso

# or - Qualquer condição verdadeira avalia toda expressão como verdaeira
# Se qualquer valor for verdadeiro, a expressão inteira sera avaliada naquele valor

# not - Serve para inverter as expressões
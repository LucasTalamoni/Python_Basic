
# Range - (start, stop, step)
# range(start, stop, step)

print('Range start')
num1 = range(10)
for numero1 in num1:
    print(numero1)

print('###############')

print('Range start, stop')
num2 = range(5, 10)
for numero2 in num2:
    print(numero2)

print('###############')

print('Range start, stop, step')
num3 = range(5, 10, 2)
for numero3 in num3:
    print(numero3)
# conversão de tipos, coerção, type convertion, typecasting, coercion
# é o ato de converter um tipo em outro
#  tipos imutáveis e primitivos:
# str, int, float, bool
#OBS: Concatenar é juntar duas ou mais coisas
print(int('1'), type(int('1')))
print(type(float('1') + 1))
print(bool(' '))
print(bool(''))
#Uma string vazia é considerada falsa, e uma que não esteja vazia
#não é considerada falsa, mesmo se for somente um espaço em branco
print(str(11) + 'b')